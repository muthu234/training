#Write a program that determines the day of the week based on a user-input number using if-else statements (1 for Monday, 2 for Tuesday, etc.)
a = int(input("Enter The Day:"))
if a==1:
    print("Monday")
elif a==2:
    print("Tuesday")
elif a==3:
    print("Wednesday")
elif a==4:
    print("Thursday")
elif a==5:
    print("Friday")
elif a==6:
    print("Saturday")
else:
    print("Sunday")    
    